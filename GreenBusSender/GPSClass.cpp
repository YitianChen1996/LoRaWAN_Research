#include "GPSClass.h"

Adafruit_GPS_CPP::Adafruit_GPS_CPP(char* name, int block){
	if (-1 == (serial = open(name, O_RDWR | O_NOCTTY | O_NDELAY))) {
		printf("Unable to open %s", name);
		exit(1);
	}
	fcntl(serial,F_SETFL,0);
}

int Adafruit_GPS_CPP::serial_read(void* buffer, int length) {
	return read(serial, buffer, length);
}

int Adafruit_GPS_CPP::serial_write(void* buffer, int length) {
	return write(serial, buffer, length);
}

int Adafruit_GPS_CPP::standby(){
	char cmd[50]="$PMTK161,0*28\r\n";
	return serial_write(cmd,50);
}

int Adafruit_GPS_CPP::start(){
	char cmd[50]="$PMTK300,5000,0,0,0,0*18\r\n";
	return  serial_write(cmd,50);
}

void Adafruit_GPS_CPP::showAntenna(){
	char cmd[50]="$PGCMD,33,1*6C\r\n";
	serial_write(cmd,50);
}

